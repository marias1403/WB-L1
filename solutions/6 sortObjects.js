// Условие. Задача о сортировке объектов: у вас есть массив объектов вида { name: 'John', age: 25 }.
// Напишите код, который сортирует этот массив по возрастанию возраста,
// а при равных возрастах сортирует по алфавиту по полю name.

// Решение. Реализуем кастомную сортировку объектов, используя метод Array.prototype.sort()
// и передавая в него функцию сравнения. Эта операция называется "сортировкой с пользовательским компаратором"

// Исходный массив объектов
let people = [
  { name: "John", age: 25 },
  { name: "Alice", age: 30 },
  { name: "Bob", age: 25 },
  { name: "Eve", age: 22 }
];

// Ниже функция сравнения для сортировки
// Функция sort может принимать аргумент - компаратор.
// Компаратор - это функция, которая принимает 2 аргумента и возвращает отрицательное число,
// если первый меньше, положительное, если второй меньше и 0, если они равны.
function comparePeople(a, b) {
  // Сначала сравниваем возраст
  if (a.age < b.age) {
    return -1;
  } else if (a.age > b.age) {
    return 1;
  } else {
    // Если возрасты равны, сравниваем имена по алфавиту
    return a.name.localeCompare(b.name);
  }
}

// Вызываем метод сортировки с пользовательской функцией сравнения
people.sort(comparePeople);

// Теперь people содержит отсортированный массив объектов
console.log(people);
